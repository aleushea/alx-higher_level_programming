#!/usr/bin/python3
def add(a, b):
    """This is an addition function
    Args:
        a: first number
        b: second number
    Returns:
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """This is a subtraction function
    Args:
        a: first number
        b: second number
    Returns:
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """MThis is a multiplication function
    Args:
        a: first number
        b: second number
    Returns:
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """This is a division function
    Args:
        a: first number
        b: second number
    Returns:
        The return value. a / b
    """
    return int(a / b)
